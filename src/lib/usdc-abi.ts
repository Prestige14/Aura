export const USDC_DECIMALS = 6;
export const USDC_UNIT = BigInt(1_000_000); // 1 USDC in raw units

export const formatUSDC = (amount: bigint): string => {
  return (Number(amount) / Number(USDC_UNIT)).toFixed(2);
};

export const parseUSDC = (amount: number): bigint => {
  return BigInt(Math.round(amount * Number(USDC_UNIT)));
};
